import java.util.Scanner;
class maxofthree{

    public static void main(String[]args){
    Scanner sc = new Scanner(System.in);
    int x = sc.nextInt();
    int y = sc.nextInt();
    int z = sc.nextInt();
    if(x>y && x>z){
        System.out.println("X is greatest");
    }
    else if (y>x && y>z){
        System.out.println("Y is greatest");
    }
    else{
        System.out.println("Z is greatest");
    }
    }
}